function makediv(arr){
    const div = document.querySelector('.toolsList');
    div.innerHTML = '';
    arr.forEach(a => {
        var temp = `<div class="flip-card">
                        <div class="flip-card-inner">
                        <div class="flip-card-front">
                            ${a.name}
                        </div>
                        <div class="flip-card-back">
                            <a href="${a.link}" target="_blank" rel="noopener noreferer">${a.name}</a>
                            <p>${a.desc}</p>
                        </div>
                        </div>
                    </div>`
        div.insertAdjacentHTML('beforeend', temp);
    });
}

